    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        defaultTableModel = new DefaultTableModel();
            defaultTableModel.addColumn("Numero");
            defaultTableModel.addColumn("Descripcion");
            jTable1.setModel(defaultTableModel);
            jTable1.getColumnModel().getColumn(0).setWidth(150);
            jTable1.getColumnModel().getColumn(1).setWidth(100);
        if(!jTextField1Tienda.getText().equals(""))
        {
            if(!jTextField2Zona.getText().equals(""))
            {
                if(Integer.parseInt(String.valueOf(jSpinner1Pasillo.getValue()))>0)
                {
                    try
                    {
                        ArrayList<String> zonas = Controlador.ControladorZona.BuscarZonas(jTextField2Zona.getText(), String.valueOf(jSpinner1Pasillo.getValue()), jTextField1Tienda.getText());
                        int i=0;
                        while (i<zonas.size())
                        {
                            defaultTableModel.addRow(new Object[] {zonas.get(i),zonas.get(i+1)});
                            i=i+2;
                        }
                    }
                    catch(Exception e)
                    {
                        
                    }
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(!jTextField1Tienda.getText().equals(""))
        {
            if(Integer.parseInt(String.valueOf(jSpinner1Pasillo.getValue()))>0)
            {
                this.setVisible(false);
                RegistrarZona registrarZona = new RegistrarZona(this, String.valueOf(jSpinner1Pasillo.getValue()), jTextField1Tienda.getText());
                registrarZona.setVisible(true);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(!jTextField1Tienda.getText().equals(""))
        {
            if(Integer.parseInt(String.valueOf(jSpinner1Pasillo.getValue()))>0)
            {
                if(!jTextField2Zona.getText().equals(""))
                {
                    if(Controlador.ControladorZona.EliminarZona((String) defaultTableModel.getValueAt(select, 0), Integer.parseInt(String.valueOf(jSpinner1Pasillo.getValue())),jTextField1Tienda.getText()))
                    {
                        JOptionPane.showMessageDialog(rootPane, "Zona Eliminada", "Zona", JOptionPane.INFORMATION_MESSAGE);
                        defaultTableModel.removeRow(select);
                        jTable1.setModel(defaultTableModel);
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(rootPane, "Error al Eliminar", "Zona", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

