    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        // TODO add your handling code here:
        jComboBox1.removeAllItems();
        if(!jTextField1.getText().equals(""))
        {
            if(jRadioButton1.isSelected())
            {
                clientenaturales = Controlador.ControladorElegirCliente.ConsultarClientesNaturales(jTextField1.getText());
                int i=0;
                while(i<clientenaturales.size())
                {
                    i++;
                    jComboBox1.addItem(clientenaturales.get(i));
                    i++;
                }
            }
            else
            {
                if(jRadioButton2.isSelected())
                {
                    clientejuridicos = Controlador.ControladorElegirCliente.ConsultarClientesJuridicos(jTextField1.getText());
                    int i=0;
                    while(i<clientejuridicos.size())
                    {
                        jComboBox1.addItem(clientejuridicos.get(i));
                        i++;
                    }
                }
            }
        }
    }//GEN-LAST:event_jTextField1KeyReleased
    private static ArrayList<String> clientejuridicos;
    private static ArrayList<String> clientenaturales;
