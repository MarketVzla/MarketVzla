    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int posicioncedula=jComboBox1.getSelectedIndex()+(jComboBox1.getSelectedIndex()-1)+1;
        
        if(!jComboBox1.getSelectedItem().toString().equals(""))
        {
            if(jRadioButton1.isSelected())
            {
                Compra compra = new Compra(this, elegirTiendav, tiendav, clientenaturales.get(posicioncedula));
                compra.setVisible(true);
                this.dispose();
            }
            else
            {
                if(jRadioButton2.isSelected())
                {
                    Presupuesto presupuesto = new Presupuesto(this, jComboBox1.getSelectedItem().toString());
                    presupuesto.setVisible(true);
                    this.dispose();
                }
            }
            
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

