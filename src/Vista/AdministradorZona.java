    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
                if(!jTextField2Zona.getText().equals(""))
                {
                    if(Controlador.ControladorZona.EliminarZona((String) defaultTableModel.getValueAt(select, 0), Integer.parseInt(jComboBox2.getSelectedItem().toString()),jComboBox1.getSelectedItem().toString()))
                    {
                        JOptionPane.showMessageDialog(rootPane, "Zona Eliminada", "Zona", JOptionPane.INFORMATION_MESSAGE);
                        defaultTableModel.removeRow(select);
                        jTable1.setModel(defaultTableModel);
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(rootPane, "Error al Eliminar", "Zona", JOptionPane.ERROR_MESSAGE);
                    }
                }
            
        
    }//GEN-LAST:event_jButton3ActionPerformed

