    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        defaultTableModel = new DefaultTableModel();
            defaultTableModel.addColumn("Numero");
            defaultTableModel.addColumn("Descripcion");
            jTable1.setModel(defaultTableModel);
            jTable1.getColumnModel().getColumn(0).setWidth(150);
            jTable1.getColumnModel().getColumn(1).setWidth(100);
            if(!jTextField2Zona.getText().equals(""))
            {
                    try
                    {
                        ArrayList<String> zonas = Controlador.ControladorZona.BuscarZonas(jTextField2Zona.getText(), jComboBox2.getSelectedItem().toString(), jComboBox1.getSelectedItem().toString());
                        int i=0;
                        while (i<zonas.size())
                        {
                            defaultTableModel.addRow(new Object[] {zonas.get(i),zonas.get(i+1)});
                            i=i+2;
                        }
                    }
                    catch(Exception e)
                    {
                        
                    }
                
            }
        
    }//GEN-LAST:event_jButton1ActionPerformed

