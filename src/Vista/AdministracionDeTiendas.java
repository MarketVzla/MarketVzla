    private void jButton1EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1EliminarActionPerformed
        // TODO add your handling code here:
        try{
            if(jTable1.getSelectedRow()>=0 && jTable1.getSelectedRow()<=9)
            {
                
                if(Controlador.ControladorTienda.EliminarTienda((String) defaultTableModel.getValueAt(select, 0)))
                {
                    JOptionPane.showMessageDialog(rootPane, "Tienda Eliminada", "Tienda", JOptionPane.INFORMATION_MESSAGE);
                    defaultTableModel.removeRow(select);
                    jTable1.setModel(defaultTableModel);
                }
                else
                {
                    JOptionPane.showMessageDialog(rootPane, "Error al Eliminar", "Tienda", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
        catch(Exception e)
        {
            
        }
        
    }//GEN-LAST:event_jButton1EliminarActionPerformed

