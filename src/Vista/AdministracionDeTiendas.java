    private void jTextField_NombreTiendaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField_NombreTiendaKeyReleased
        // TODO add your handling code here:
        if(evt.getKeyCode()==KeyEvent.VK_ENTER)
        {
            defaultTableModel = new DefaultTableModel();
            defaultTableModel.addColumn("Nombre");
            defaultTableModel.addColumn("Fecha");
            defaultTableModel.addColumn("Valor Puntos");
            jTable1.setModel(defaultTableModel);
            jTable1.getColumnModel().getColumn(0).setWidth(150);
            jTable1.getColumnModel().getColumn(1).setWidth(100);
            jTable1.getColumnModel().getColumn(2).setWidth(100);
            try{
                ArrayList<String> tiendas = Controlador.ControladorTienda.BuscarTiendas(jTextField_NombreTienda.getText());
                int i=0;
                while (i<tiendas.size())
                {
                    defaultTableModel.addRow(new Object[] {tiendas.get(i+1),tiendas.get(i+2),tiendas.get(i+3)});
                    i=i+5;
                }
                
            }
            catch(Exception e)
            {
                
            }
            
            
            
            
        }
    }//GEN-LAST:event_jTextField_NombreTiendaKeyReleased

