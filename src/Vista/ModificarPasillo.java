    private void jButton1AceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1AceptarActionPerformed
        // TODO add your handling code here:
        if(!jTextField2Descripcion.getText().equals(""))
        {
            if(Controlador.ControladorPasillo.ActualizarPasillo(Integer.parseInt(pasillo), jTextField2Descripcion.getText(), tienda))
            {
                JOptionPane.showMessageDialog(rootPane, "Pasillo Modificado", "Pasillo", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
                administradorPasillos.setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(rootPane, "Error al Actualizar", "Pasillo", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1AceptarActionPerformed

