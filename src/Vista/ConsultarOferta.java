    public ConsultarOferta(String codigo) {
        initComponents();
        ArrayList<String> oferta = Controlador.ControladorOferta.ConsultarOferta(codigo);
        jLabel6.setText(oferta.get(0));
        jLabel2.setText(oferta.get(1));
        jLabel4.setText(oferta.get(2));
        ArrayList<String> productos = Controlador.ControladorOferta.ConsultarProductosDeOferta(codigo);
        defaultTableModel=new DefaultTableModel();
        defaultTableModel.addColumn("Nombre");
        defaultTableModel.addColumn("Descuento");
        defaultTableModel.addColumn("Precio");
        jTable1.setModel(defaultTableModel);
        int i=0;
        while(i<productos.size())
        {
            defaultTableModel.addRow(new Object[]{productos.get(i),productos.get(i+1),productos.get(i+2)});
            i=i+3;
        }
    }

